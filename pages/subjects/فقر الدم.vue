                        <h5 id="hemo_cure">علاج فقر الدم</h5>
                        <p>تتنوع علاجات فقر الدم وتختلف تبعاً للمسبب، وهي على النحو التالي:</p>
                        <ul>
                            <li><b>علاج فقر الدم الناجم عن عوز الحديد:</b>
                            يتم علاج فقر الدم من هذا النوع، في أغلب الحالات، بواسطة تناول مكملات (مضافات) الحديد.
                            </li>
                            <li><b>علاج فقر الدم الناجم عن عوز الفيتامينات:</b>
                            هو نوع صعب من فقر الدم تتم معالجته بواسطة حُقن تحتوي على فيتامين B - 12، وقد يستمر ذلك، في بعض الحالات، مدى الحياة.
                            </li>
                            <li><b>علاج فقر الدم المصاحب للأمراض المزمنة:</b>
                                ليس هنالك علاج معين لهذا النوع من فقر الدم.
                            </li>
                            <li><b>علاج فقر الدم اللاتنسجي:</b>
                                قد يشمل علاج فقر الدم من هذا النوع باعطاء الدم وريديا (Intravenous feeding) لرفع كمية خلايا الدم الحمراء في الجسم.
                            </li>
                            <li><b>علاج فقر الدم الناجم عن مرض في نخاع العظم:</b>
                                يتراوح علاج فقر الدم الناجم عن هذه الأمراض المتعددة بين تناول أدوية بالمعالجة الكيماوية (Chemotherapy) وحتى زرع نخاع عظم.
                            </li>
                            <li><b>علاج فقر الدم الناجم عن انحلال الدم:</b>
                                يشمل علاج فقر الدم الناجم عن انحلال الدم الامتناع عن تناول أدوية معينة، معالجة العدوى (التلوثات) المرافقة وتناول أدوية كابتة للجهاز المناعي الذي يهاجم خلايا الدم الحمراء.
                            </li>
                            <li><b>علاج فقر الدم المنجلي:</b>
                                يشمل علاج فقر الدم من هذا النوع، مراقبة ورصد مستويات الأكسجين في الجسم، تناول مسكنات للآلام، تناول السوائل، بالشرب أو عن طريق التسريب لتخفيف الآلام ولمنع ظهور مضاعفات.
                            </li>
                        </ul>
