                        <h6><b>انواع خلايا الدم:</b></h6>
                        <p>هنالك ثلاثة أنواع مختلفة من خلايا الدم التي تعوم داخل البلازما:</p>
                        <ol>
                            <li><b>خلايا/ كريات الدم البيضاء (Leucocytes):</b>هذه الخلايا تحارب العدوى (Infections).</li>
                            <li><b>خلايا/ كريات الدم الحمراء (Erythrocytes): </b>هذه الخلايا تحمل الأكسجين (Oxygen) من الرئتين، بواسطة دفق الدم، إلى الدماغ والأعضاء الحيوية الأخرى والأنسجة في الجسم.</li>
                        </ol>
                        <h5 id="hemo_importance">أهمية الهيموغلوبين</h5>
                        <p>
                            تحتوي خلايا الدم الحمراء على الهيموجلوبين (خِضاب الدم - Hemoglobin)، وهو بروتين غني بالحديد أحمر اللون يمنح الدم لونه المعتاد(الأحمر).
                            الهيموجلوبين يُمكِّن خلايا الدم الحمراء من نقل (حمل) الأكسجين من الرئتين إلى بقية أعضاء الجسم ونقل ثاني أكسيد الكربون (Carbon dioxide) من الجسم، عودةً، إلى الرئتين، بحيث يمكن إخراجه من الجسم في عملية الزفير.
                            معظم خلايا الدم، بما فيها خلايا الدم الحمراء، يتم إنتاجها باستمرار في النِقي (نخاع العظم - Bone marrow)، وهو عبارة عن مادة إسفنجية حمراء اللون موجودة في داخل تجويفات العظام الكبيرة في الجسم.
                            من أجل إنتاج الهيموجلوبين وخلايا الدم الحمراء، يحتاج الجسم إلى الحديد ومعادن أخرى وفيتامينات وبروتينات تتوفر في الغذاء الذي يتناوله الإنسان. بالإضافة الى بعض الهورمونات أهمها هرمون الاريتروبوئيتين (EPO) والتي تقوم الكلى بإفرازه من أجل انتاج خلايا الدم الحمراء.
                            تجدر الإشارة إلى أن معدّل عمر خلية الدم الحمراء يتراوح ما بين ال 110 إلى 120.
                            عندما يعاني الإنسان من الأنيميا، فإن جسمه لا ينتج ما يكفي من خلايا الدم الحمراء، إنما يُضيّع الكثير منها أو يتلفها بسرعة تفوق قدرته على إنتاج خلايا دم جديدة.
                        </p>
                        <h5>انواع فقر الدم</h5>
                        <p>الأنواع الشائعة من الأنيميا - فقر الدم - ومسبباتها تشمل:</p>
                        <ul>
                            <li><b>فقر الدم الناجم عن عَوَز الحديد (Iron deficiency anemia):</b>
                                هذا النوع الشائع من فقر الدم يصيب ما يقارب 2 – 3% من سكان الولايات المتحدة البالغين. والسبب لحدوثه هو نقص الحديد في الجسم. ذلك أن نخاع العظم يحتاج إلى الحديد من أجل إنتاج الهيموجلوبين، وإن لم يكن تزويد الحديد كافيا، فلن يستطيع الجسم إنتاج كمية كافية من الهيموجلوبين لخلايا الدم الحمراء.
                            </li>
                            <li><b>فقر الدم الناجم عن عوز الفيتامينات (Vitamin Deficiency Anemia):</b>
                                بالإضافة إلى حاجته إلى الحديد، يحتاج الجسم أيضا إلى حمض الفوليك (Folic acid) وإلى فيتامين "ب 12" (Vitamin B12) لإنتاج كمية كافية من خلايا الدم الحمراء الصحيحة. النظام الغذائي الذي ينقصه أحد المركبات الغذائية الحيوية، يمكن أن يؤدي إلى خفض إنتاج خلايا الدم الحمراء. هناك بعض الأشخاص الذين يعانون من عدم قدرة جسمهم على امتصاص فيتامين B-12  بشكل فعال.
                            </li>
                            <li><b>فقر الدم كعَرَض لمرض مزمن:</b>
                                الإصابة بالعديد من الأمراض المزمنة، مثل السرطان (Cancer)، الإيدز (متلازمة العوز المناعي المكتسب - AIDS)، مرض النقرس (Gout)، داء كرون (Crohn’s disease)، وأمراض التهابية مزمنة أخرى، بإمكانها التأثير على إنتاج خلايا الدم الحمراء، والتسبب بالتالي بفقر دم مزمن. كما يمكن أن تؤدي الإصابة بفشل الكلى أيضا، إلى فقر الدم.
                            </li>
                            <li><b> فقر الدم اللَّاتَنَسُّجِي ُّ(Aplastic anemia):</b>
                                هذا النوع من فقر الدم نادر جدا، لكنه يشكل خطرا على الحياة. وهو يحدث نتيجة لتدني قدرة نخاع العظم على إنتاج أنواع خلايا الدم الثلاثة (خلايا الدم الحمراء، البيضاء وصفائح الدم). ولا يزال سبب فقر الدم اللاتنسجي غير معروف في معظم الحالات. ولكن، هنالك اعتقاد بأن له علاقة بأمراض تمس بعمل الجهاز المناعي (Immune system).
                            </li>
                            <li><b> فقر الدم الناجم عن مرض في نخاع العظم:</b>
                                العديد من الأمراض، مثل ابيضاض الدم (اللوكيميا - Leukemia)، خلل التنسج النخاعي (Myelodysplasia) تكون عبارة عن حالة طبية سابقة للوكيميا ويمكنها أن تؤدي إلى ظهور فقر دم والتأثير على نخاع العظم. تأثيرات الاضطرابات السرطانية- أو شبه السرطانية، هذه يمكن أن تتراوح بين التغيير المعتدل جدا في كميات إنتاج خلايا الدم وحتى التوقف الكامل عن إنتاجها بما يشكل خطرا على الحياة. كما أن هنالك أنواعا أخرى من السرطان تهاجم الدم ونخاع العظم، مثل الورم النقيي المتعدد (Multiple myeloma)، اضطرابات التكاثر النقوي (Myeloproliferative disorders)، الورم اللمفي (lymphoma).
                            </li>
                            <li><b> فقر الدم الناجم عن انحلال الدم (Hemolysis):</b>
                                هذه المجموعة من أنواع فقر الدم تتطور عندما يتم إتلاف خلايا الدم الحمراء بسرعة تفوق قدرة نخاع العظم على إنتاج خلايا دم جديدة.
                            </li>
                            <li><b> الإصابة ببعض الأمراض:</b>
                                هنالك أمراض دم معينة بإمكانها التسبب بتلف خلايا الدم الحمراء بشكل مكثف. الاضطرابات في الجهاز المناعي يمكن أن تدفع الجسم إلى إنتاج أضداد (Antibodies) لخلايا الدم الحمراء، والتسبب بإتلافها قبل الأوان. كما أن تناول أدوية معينة، كأنواع مختلفة من المضادات الحيوية (Antibiotics) المستعملة لمعالجة أنواع مختلفة من العدوى (التلوثات)، يمكن أن يتسبب، أيضا، بتلف خلايا الدم الحمراء.
                            </li>
                            <li><b> فقر الدم المنجلي (Sickle cell anemia):</b>
                                هذا النوع من فقر الدم يكون أحيانا حادًا، ينتقل بالوراثة، وفي معظم الحالات يصيب الناس ذوي الأصول الإفريقية، العربية أو بحر المتوسطية. هذا النوع من فقر الدم يحدث نتيجة لخلل في الهيموجلوبين يجعل خلايا الدم الحمراء ذات شكل استثنائي يشبه المنجل. هذا الشكل من خلايا الدم الحمراء يسبب ضمور هذه الخلايا وموتها قبل الأوان، وهكذا يسبب نقصا مزمنا في خلايا الدم الحمراء.
                            </li>
                            <li><b> أنواع أخرى من فقر الدم:</b>
                                هنالك أنواع إضافية أخرى من فقر الدم، هي أنواع أكثر ندرة مثل مرض الثلاسيميا (Thalassemia)، وأنواع تنجم عن عيوب في الهيموجلوبين. وأحيانا، لا يمكن الوصول إلى تشخيص دقيق لسبب ظهور الأنيميا.
                            </li>
                        </ul>
                        <h5 id="hemo_danger">عوامل خطر الإصابة بفقر الدم</h5>
                        <p>تشمل عوامل الخطر الإصابة بفقر الدم بما يلي:</p>
                        <ul>
                            <li><b>سوء التغذية:</b>
                                كل إنسان، سواء كان شابًا أو بالغًا، تتركب تغذيته، على الغالب، من أغذية فقيرة الحديد والفيتامينات، وبالأخص حمض الفوليك، معرض للإصابة بفقر الدم. فالجسم بحاجة إلى الحديد، البروتين والفيتامينات لكي يُنتج كمية كافية من خلايا الدم الحمراء.
                            </li>
                            <li><b>أمراض واضطرابات في الأمعاء:</b>
                                إن المصابين بأمراض أو اضطرابات في الأمعاء من شأنها التأثير على امتصاص مركبات الغذاء في الأمعاء الدقيقة، مثل داء كرون والداء البطني (Celiac disease)، هم أشخاص معرضون للإصابة بفقر الدم. إجراء عملية لإزالة جزء من الأمعاء الدقيقة، أو معالجة الجزء المصاب في الأمعاء الدقيقة، التي يتم فيها امتصاص مركبات الغذاء، يمكن أن يؤدي إلى نقص في مركبات غذائية معينة، ثم إلى فقر الدم تاليا.
                            </li>
                            <li><b>الدورة الشهرية (دورة الحيض - Menstrual cycle): </b>
                                النساء في سن الخصوبة هن، بشكل عام، الفئة الأكثر عرضة للإصابة بفقر الدم الناجم عن عوز الحديد، مقارنة بالرجال. وذلك لأن المرأة تفقد كميات من الدم، ونتيجة لذلك تفقد الحديد أيضا، في فترة الحيض.
                            </li>
                            <li><b>الحمل: </b>
                                المرأة الحامل تكون معرضة جدا للإصابة بفقر الدم الناجم عن عوز الحديد، وذلك لأن مخزون الحديد المتوفر لديها ينبغي أن يدعم حجما أكبر من الدم مما في الحالة الطبيعية وأن يكون، أيضا، مصدرًا للهيموجلوبين الذي يحتاج إليه الجنين للنمو والتطور.
                            </li>
                            <li><b>أمراض وحالات مزمنة: </b>
                                المصابون بمرض سرطانيّ، بالفشل الكلوي (Renal failure) أو بالفشل الكبدي (Hepatic failure)، أو بأية حالة طبية مزمنة أخرى، يمكن أن يكونوا عرضة لتطور فقر دم يدعى "فقر الدم المصاحب لأمراض مزمنة" (فقر الدم كعَرَض لمرض مزمن).
                                هذه الحالات الطبية قد تتسبب بنقص في خلايا الدم الحمراء. كما أن فقدان الدم، البطيء لكن المزمن، الناجم عن قرحة هضمية (Peptic ulcer)، أو عن قرحة في مكان آخر من الجسم، قد يؤدي إلى استنفاد مخزون الحديد في الجسم، وبالتالي يُسبب فقر دم الناجم عن عوز الحديد.
                            </li>
                            <li><b>عوامل وراثية:</b>
                                إذا كان ثمة من بين أفراد العائلة من هو مصاب بإحدى حالات فقر الدم التي تنتقل بالوراثة، كفقر الدم المنجلي، فهذا يشكل عامل خطر للإصابة بفقر الدم، على أساس وراثي.
                            </li>
                        </ul>
                        <h5 id="hemo_complication">مضاعفات فقر الدم</h5>
                        <p>إذا لم يتم علاج الانيميا، فقد يؤدي إلى ظهور تأثيرات ومضاعفات جانبية مختلفة، منها:</p>
                        <ul>
                            <li><b>فرط التعب (التعب الزائد):</b>
                                في حالات فقر الدم الصعبة من الممكن أن يشعر المريض بالتعب لدرجة تمنعه من القيام بواجباته اليومية البسيطة جدا، إذ يكون متعبا جدا ويصعب عليه اللعب أو العمل
                            </li>
                            <li><b>مشاكل في القلب:</b>
                                قد يسبب فقر الدم تسارع نبضات القلب وعدم انتظامها - اضطراب نظم القلب (Arrhythmia). ففي حالة فقر الدم يتوجب على القلب أن يضخ كمية أكبر من الدم من أجل التعويض عن نقص الأكسجين في الدم. وقد يؤدي هذا الأمر، حتى، إلى "فشل القلب" (Heart failure).
                            </li>
                            <li><b>ضرر للأعصاب:</b>
                                يعتبر فيتامين B - 12 ضروريا وحيويا ليس فقط لإنتاج خلايا الدم الحمراء السليمة، وإنما ايضا لعمل جهاز الأعصاب بشكل سليم.
                            </li>
                            <li><b>تغيرات في الحالة الإدراكية:</b>
                                النقص في فيتامين B - 12 قد يؤثر على عمل الدماغ الطبيعي.
                            </li>
                            <li><b>الموت:</b>
                                أنواع معينة من فقر الدم التي تنتقل بالوراثة، مثل فقر الدم المنجلي، قد تسبب مضاعفات طبية حادة تشكل خطرا على الحياة. فقدان كميات كبيرة من الدم خلال فترة زمنية قصيرة يؤدي، بصورة عامة، إلى فقر دم، قد يكون قاتلا.
                            </li>
                        </ul>
